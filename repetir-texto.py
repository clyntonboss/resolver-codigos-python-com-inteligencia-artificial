# repetir_texto.py

# Solicita uma string ao usuário
texto = input("Digite uma palavra ou frase: ")

# Solicita um número inteiro
vezes = int(input("Quantas vezes deseja repetir? "))

# Repete a string o número de vezes informado
resultado = texto * vezes

# Exibe o resultado
print("Resultado da repetição:")
print(resultado)
